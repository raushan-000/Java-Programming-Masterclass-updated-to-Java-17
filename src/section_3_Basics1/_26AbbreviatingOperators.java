package section_3_Basics1;

public class _26AbbreviatingOperators {
    public static void main(String[] args) {
        int number = 10;
        number = number+1;  //11
        System.out.println(number);
        number++; //12
        System.out.println(number);
        number--; //11
        System.out.println(number);
        number = number-1; //10
        System.out.println(number);
    }
}
