package section_3_Basics1;

public class _25Operator {
    public static void main(String[] args) {
        char firstChar = 'A';
        char secondChar = 'B';
        System.out.println(firstChar+secondChar);
        System.out.println(""+firstChar+secondChar);

        int myNumber1 = 12;
        int myNumber2 = 10;
        System.out.println(myNumber1+myNumber2);
        System.out.println(myNumber1-myNumber2);
        System.out.println(myNumber1*10);
        System.out.println(myNumber1/2);
        System.out.println(myNumber2%4);
    }
}
